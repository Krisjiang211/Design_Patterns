/**
 * 享元模式
 * 享元模式是一种结构型设计模式，它通过共享对象来减少内存使用和提高性能。
 * 其实类似于工厂模式，工厂模式是创建型模式，它的作用是创建对象。
 * 而享元模式是结构型模式，它的作用是减少对象的创建，通过共享对象来减少内存使用和提高性能。
 * 享元模式的核心是享元工厂，享元工厂是一个工厂，它的作用是创建享元对象。
 * 享元对象是共享的，也就是说，多个客户端可以共享同一个享元对象。
 * 享元对象的状态是不变的，也就是说，享元对象的状态不能改变。
 * 享元对象的状态是通过参数传递给享元对象的。
 */


